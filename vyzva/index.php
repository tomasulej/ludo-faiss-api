                <h2>Čo chceme urobiť?</h2>

                <p>Chceme <strong>dostať na internet zhruba 50-tisíc ľudových piesní</strong>. Zberatelia prešli celé Slovensko, zozbierali ich a dali do veľkých a hrubých kníh, na ktoré sa teraz práši v knižniciach. Chceme, aby <strong>boli každému prístupné cez internet</strong> - ku každej nielen noty a text, ale aj nahrávka, a množstvo údajov, ktoré k nim zberatelia pridali: informácie o tom, kde vznikli, kedy, alebo pri akej príležitosti ich ľudia spievali. Vznikne tak <strong>najrozsiahlejšia databáza našej hudby voľne dostupná každému</strong>.</p>

